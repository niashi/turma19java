package programas;

public class PrimeirosPassos {
	
	
	/* Copiando esse programa procedural para o Java:
	 * 
	 * // Variáveis
		inteiro valor1, valor2, soma

		// Entrada
		valor1 = 10
		valor2 = 30

		// Processamento
		soma = valor1 + valor2

		// Saída
		escreva("A soma é " + soma + ".\n")
	 */
	
	public static void main(String[] args) {
		
		// Declaração de variáveis
		int valor1 = 10, valor2 = 20, soma = 0;
		
		// Processamento
		soma = valor1 + valor2;
		
		// Saída
		System.out.println("O primeiro valor é " + valor1 + ".\n");
		System.out.println("O segundo valor é " + valor2 + ".\n");
		System.out.println("A soma é dos valores é " + soma + ".\n");
		
	}
}
